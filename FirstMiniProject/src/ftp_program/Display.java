package ftp_program;

public class Display {

	public static  void rabbit(int i) {
		switch (i) {
		case 1:
			System.out.println("|/)");
			break;
		case 2:
			System.out.println("|')\n");
			break;
		case 3:
			System.out.println("|/)_/)");
			break;
		case 4:
			System.out.println("| 'ㅅ') \n");
			break;
		case 5:
			System.out.println("|/)_/)");
			break;
		case 6:
			System.out.println("| 'ㅅ')っ♥");
			break;
		case 7:
			System.out.println("\n\n");
			i = 0;
			break;
		}
	}
}

